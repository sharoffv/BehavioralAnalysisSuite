    'FontWeight','bold','ForegroundColor',[0 0 0],'Units','Normalized',...
    'Position',[.605 .01 .16 .03],'String','Export Plot(s)','Style','pushbutton',...
    'BackgroundColor',[0.7969 0.7969 0.7969]);
posVelGUIData.printFig = uicontrol('FontName','Arial','FontSize',15,...
    'FontWeight','bold','ForegroundColor',[0 0 0],'Units','Normalized',...
    'Position',[.79 .01 .16 .03],'String','Print Plot(s)','Style','pushbutton',...
    'BackgroundColor',[0.7969 0.7969 0.7969]);

set(posVelGUIData.figHandle,'OuterPosition',[scrn(1)+0.5*(scrn(3)-scrn(1)) scrn(2) ...
    0.5*(scrn(3)-scrn(1)) scrn(4)]);
end